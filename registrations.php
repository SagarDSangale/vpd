											<div id="collapse2" class="panel-collapse collapse">
												<div class="panel-body">
													<div class="at-description">
														<p>Click on <b>Get Quotation</b> tab above and fill the details, you will receive required documents list &amp; quotation in few seconds in your mailbox.</p>
