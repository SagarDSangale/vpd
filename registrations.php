										<div class="panel panel-default">
											<div class="panel-heading">
												<h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse3">3. What is meant by an article under the Designs Act, 2000?</a></h4>
											</div>
											<div id="collapse3" class="panel-collapse collapse">
												<div class="panel-body">
													<div class="at-description">
														<p> Under the Designs Act, 2000 the "article" means any article of manufacture and any substance, artificial, or partly artificial and partly natural; and includes any part of an article capable of being made and sold separately.</p>
														
													</div>
												</div>
											</div>
										</div>
										<div class="panel panel-default">
											<div class="panel-heading">
												<h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse4">4. What is the object of registration of Designs?</a></h4>
											</div>
											<div id="collapse4" class="panel-collapse collapse">
												<div class="panel-body">
													<div class="at-description">
														<p> Object of the Designs Act to protect new or original designs so created to be applied or applicable to particular article to be manufactured by Industrial Process or means. Sometimes purchase of articles for use is influenced not only by their practical efficiency but also by their appearance. The important purpose of design Registration is to see that the artisan, creator, originator of a design having aesthetic look is not deprived of his bonafide reward by others applying it to their goods.</p>
														
													</div>
												</div>
											</div>
										</div>
										<div class="panel panel-default">
											<div class="panel-heading">
												<h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse5">5. Can stamps. Labels, tokens, be considered an article for the purpose of registration?</a></h4>
											</div>
											<div id="collapse5" class="panel-collapse collapse">
												<div class="panel-body">
													<div class="at-description">
														<p> No. Because once the alleged Design i.e., ornamentation is removed only a piece of paper, metal or like material remains and the article referred ceases to exist. Article must have its existence independent of the Designs applied to it. [Design with respect to label was held not registrable, by an Order on civil original case No. 9-D of 1963, Punjab, High Court]. So, the Design as applied to an article should be integral with the article itself.
				      </p>
														
													</div>
												</div>
											</div>
										</div>
										<div class="panel panel-default">
											<div class="panel-heading">
												<h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse6">6. When does the Applicant for Registration of Design get the registration certificate?</a></h4>
											</div>
											<div id="collapse6" class="panel-collapse collapse">
												<div class="panel-body">
													<div class="at-description">
														 <p> When an application for registration of a Design is in order, it is accepted and registered and then a certificate of registration is issued to the applicant.
				      </p>
				     <p> However, a separate request should be made to the Controller for obtaining a certified copy of the certificate for legal proceeding with requisite fee.
				      </p>
														
													</div>
												</div>
											</div>
										</div>
										<div class="panel panel-default">
											<div class="panel-heading">
												<h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse7">7. What is a Register of Designs?</a></h4>
											</div>
											<div id="collapse7" class="panel-collapse collapse">
												<div class="panel-body">
													<div class="at-description">
														<p> The Register of Designs is a document maintained by The Patent Office, Kolkata as a statutory requirement. It contains the design number, class number, date of filing (in this country) and reciprocity date (if any), name and address of Proprietor and such other matters as would affect the validity of proprietorship of the design and it is open for public inspection on payment of prescribed fee &amp; extract from register may also be obtained on request with the prescribed fee.
				      </p>
														
													</div>
												</div>
											</div>
										</div>
										<div class="panel panel-default">
											<div class="panel-heading">
												<h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse8">8. What is the effect of registration of design?</a></h4>
											</div>
											<div id="collapse8" class="panel-collapse collapse">
												<div class="panel-body">
													<div class="at-description">
														<p> The registration of a design confers upon the registered proprietor 'Copyright' in the design for the period of registration. 'Copyright' means the exclusive right to apply a design to the article belonging to the class in which it is registered.
				      </p>
														
													</div>
												</div>
											</div>
										</div>
										<div class="panel panel-default">
											<div class="panel-heading">
												<h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse9">9. What is the duration of the registration of a design?</a></h4>
											</div>
											<div id="collapse9" class="panel-collapse collapse">
												<div class="panel-body">
													<div class="at-description">
														 <p> The duration of the registration of a design is initially ten years from the date of registration, but in cases where claim to priority has been allowed the duration is ten years from the priority date.<br>
					This initial period of registration may be extended by further period of 5 years on an application made in Form accompanied by a fee of Rs. 2,000/- to the Controller before the expiry of the said initial period of Copyright.<br>
					The proprietor of a design may make application for such extension even as soon as the design is registered.
				      </p>
														
													</div>
												</div>
											</div>
										</div>
										<div class="panel panel-default">
											<div class="panel-heading">
												<h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse10">10. What is the date of registration?</a></h4>
											</div>
											<div id="collapse10" class="panel-collapse collapse">
												<div class="panel-body">
													<div class="at-description">
														 <p> The date of registration except in case of priority is the actual date of filing of the application. In case of registration of design with priority, the date of registration is the date of making an application in the reciprocal country.
				      </p>
														
													</div>
												</div>
											</div>
										</div>
										<div class="panel panel-default">
