												<h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapse11">11. Is it possible to re-register a design in respect of which Copyright has expired?</a></h4>
											</div>
											<div id="collapse11" class="panel-collapse collapse">
												<div class="panel-body">
													<div class="at-description">
														 <p> No. A registered design, the copyright of which has expired cannot be re-registered.
				      </p>
														
